/**
  * @brief This function handles EXTI line[15:10] interrupts.
  */
void EXTI15_10_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI15_10_IRQn 0 */
	if(state==0){ //riconosco la prima pressione del pulsante
		__HAL_TIM_SET_COUNTER(&htim5, 0);
					HAL_TIM_Base_Start_IT(&htim5);//avvio il timer5 per ottenere
			HAL_UART_Transmit(&huart2, "START\n\r", 7, 1000);
			HAL_TIM_Base_Stop_IT(&htim4);//trasmissione in seriale tramite la huart2
			state=1;//passaggio di stato
			}
	else if(state==1){ //riconosco la seconda pressione del pulsante

			HAL_UART_Transmit(&huart2, "PWM 1 ON\n\r", 10, 1000);//trasmissione in seriale tramite la huart2
			state=2;//passaggio di stato
		}
	else if(state==2){ //terza pressione del pulsante

			HAL_UART_Transmit(&huart2, "PWM 2 ON\n\r", 10, 1000);//trasmissione in seriale tramite la huart2
			state=3;//passaggio di stato
		}
	else if(state==3){ //terza pressione del pulsante

			HAL_UART_Transmit(&huart2, "PWM 3 ON\n\r", 10, 1000);//trasmissione in seriale tramite la huart2
			state=0;//passaggio di stato
			}


  /* USER CODE END EXTI15_10_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(B1_Pin);
  /* USER CODE BEGIN EXTI15_10_IRQn 1 */

  /* USER CODE END EXTI15_10_IRQn 1 */
}

/**
  * @brief This function handles TIM5 global interrupt.
  */
void TIM5_IRQHandler(void)
{
  /* USER CODE BEGIN TIM5_IRQn 0 */
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, 1);//impulso alto sul pin5
	if(state==0){
		__HAL_TIM_SET_COUNTER(&htim4, 0);//settiamo il timer a 0
		HAL_TIM_Base_Start_IT(&htim4); //avvio timer
		HAL_TIM_Base_Stop_IT(&htim3);//stop del timer utilizzato nello stato precedente

		}
	else if(state==1){
		HAL_TIM_Base_Stop_IT(&htim4);



	}
	else if(state==2){
		__HAL_TIM_SET_COUNTER(&htim2, 0);
		HAL_TIM_Base_Start_IT(&htim2);

		}
	else if(state==3){
		__HAL_TIM_SET_COUNTER(&htim3, 0);
		HAL_TIM_Base_Start_IT(&htim3);
		HAL_TIM_Base_Stop_IT(&htim2);

		}



  /* USER CODE END TIM5_IRQn 0 */
  HAL_TIM_IRQHandler(&htim5);
  /* USER CODE BEGIN TIM5_IRQn 1 */

  /* USER CODE END TIM5_IRQn 1 */
}

